# Chapter 6 - Strings
# 6.2 Getting the length of a string using len

# File: get_string_length.py
# Description - This example gets the Length of a string using the len function.
# len is a built-in function that returns the number of characters in a string

# Declare a variable called fruit and assign it a string of banana.
fruit = 'banana'
# The sequence stored in variable fruit is passed to the len function.
# The len function returns the length of the string.
# The string length is then saved in variable letter
letter = len(fruit)
# display the value stored in variable letter in the console.
print(letter)