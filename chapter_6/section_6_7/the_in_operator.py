# 6.7 The in operator

# File: the_in_operator.py
# Description - Check a string for an occurrence of another string using in operator.

# The word in is a boolean operator that takes two strings and returns True if the
# first appears as a substring in the second.

# checks if the string a exists in string banana. If so it returns true, otherwise false.
# Since string a is in banana, this returns true.
print('a' in 'banana')

# checks if the string a exists in string banana. If so it returns true, otherwise false.
# Since string seed is not in banana, this returns false.
print('seed' in 'banana')