# File: string_method_find.py
# Description - Use the find method to searche for the position of one string within another.

# Declare a variable called word and assign it a string of banana.
word = 'banana'

# Use the find method to find the index of the string a inside the string saved in variable called word
# and save it in the variable called index.
index = word.find('a')

# Print the value stored in variable index to the console.
# The first letter a is in index 1 so the value stored in variable index is 1
print(index)



