# 6.9 String methods

# File: string_method_upper.py
# Description - Use the upper method to capitalize all letter of a string.

# Declare a variable called word and assign it a string of banana.
word = 'banana'

# Use the upper method to capitalize all letters of the string and save it in variable called new_word.
new_word = word.upper()
# Print the value stored in variable new_word to the console.
print(new_word)



